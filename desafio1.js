function mostrarNome(nome) {
    console.log(`Olá! ${nome}!`);
}

function mostrarDobro(numero) {
    return numero * 2;
}

function mostrarMediaTripla(valor1, valor2, valor3) {
    return (valor1 + valor2 + valor3) / 3;
}

function mostrarMaior(num1, num2) {
    return num1 > num2 ? num1 : num2;
}

function mostrarMultiplicacao(numx) {
    return numx * numx;
}